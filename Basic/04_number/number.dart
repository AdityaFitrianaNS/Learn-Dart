void main() {
  int number1 = 10;
  int number2 = 11;
  double number3 = 10.5;
  double number4 = 11.5;

  print('Hasil : ');
  print(number1);
  print(number2);
  print(number3);
  print(number4);

  // Menubah ke operator penjumlahan dan perkalian
  number1 = 12 + 9 * 2;
  number2 = 14 + 8 * 2;
  number3 = 12.5 * 2.5;
  number4 = 13.5 * 2.5;

  print('----------');
  print('Hasil : ');
  print(number1);
  print(number2);
  print(number3);
  print(number4);
  print('----------');

  // menggunakan num
  num number = 10;
  print('Hasil : ');
  print(number);

  number = 10.5;
  print('Hasil : ');
  print(number);

  number = 10.5 + 10;
  print('Hasil : ');
  print(number);
}
